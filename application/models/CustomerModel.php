<?php

defined('BASEPATH') OR exit('No direct script access allowed');

class CustomerModel extends CI_Model {

    function show($id_customer = null, $nama_customer = null)
    {
      $this->db->select('*')->from('customer');

      if($id_customer != null){
        $this->db->where('id_customer', $id_customer);
      }

      if($nama_customer != null){
        $this->db->like('nama_customer', $nama_customer);
      }

      $this->db->order_by('tgl_input', 'desc');
      return $this->db->get();
    }

    function add($data, $log)
    {
      $this->db->trans_start();
      $this->db->insert('customer', $data);
      $this->db->insert('log', $log);
      $this->db->trans_complete();

      if ($this->db->trans_status() === FALSE){
        $this->db->trans_rollback();
        return false;
      } else {
        $this->db->trans_commit();
        return true;
      }
    }

    function edit($param, $data, $log)
    {
      $this->db->trans_start();
      $this->db->where('id_customer', $param)->update('customer', $data);
      $this->db->insert('log', $log);
      $this->db->trans_complete();

      if ($this->db->trans_status() === FALSE){
        $this->db->trans_rollback();
        return false;
      } else {
        $this->db->trans_commit();
        return true;
      }
    }

    function delete($param, $log)
    {
      $this->db->trans_start();
      $this->db->where('id_customer', $param)->delete('customer');
      $this->db->insert('log', $log);
      $this->db->trans_complete();

      if ($this->db->trans_status() === FALSE){
        $this->db->trans_rollback();
        return false;
      } else {
        $this->db->trans_commit();
        return true;
      }
    }

}

?>
